// 5. 배열 : 여러개 자료들을 *순서*대로 저장하는 *자료* 배열도 자료다.
let ary1 = [ '봄','여름','가을','박진감'];
// 1) let ary1(아무거나) : 변수명 (선언/만들기)
// 2) = : 대입, 오른쪽 자료를 왼쪽에 넣기
// 3) '봄','여름','가을','박진감' : 자료
// 4) [] : 배열

// 6. 인덱스 : 배열 내 저장된 순서번호 *0번시작*, 중간에 삭제되더라도 한 칸씩 당겨진다.
console.log(ary1[3]); // 변수명[인덱스] 변수 안에 있는 배열의 3번쨰 자리에 있는 박진감이 출력됨.

