console.log(ary1); // 가을이 삭제된 걸 알 수 있음!!!

// 10. 배열  내 자료(요소) 중간 삽입 : 변수명.splice( 삽입할 인덱스 , 0 , 새로운 자료 )
ary1.splice( 2 , 0 , '가을'); console.log(ary1); // 0개를 삭제하고 추가만 해낸 것임.

// 11. 배열 내 자료(요소) 값 찾기 : 변수명.indexof( 찾을 값 ) , 찾을 값이 존재하면 인덱스 반환 , 없으면 -1
let result = ary1.indexOf('가을'); console.log( result );  //앞에 let result 변수를 만든 이유 : 출력해서 보고 싶으니께.

// 12. 배열 내 자료 개수 반환 :  변수명.length , 배열 내 자료(요소) 총 개수 반환
console.log( ary1.length ) // length는 소괄호를 안 씀!


