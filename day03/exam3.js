// 7. 배열 내 자료(요소) 수정 : 변수명[수정할 인덱스] = 새로운 값

ary1[ 0 ] = 'SPRING';
console.log(ary1[0]);

// 8. 배열 내 자료(요소) 추가 : 변수명.push( 새로운 값 ) , 마지막 인덱스 뒤로 추가된다.

