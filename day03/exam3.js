// ; 세미콜론이란? 한 문장의 명령어 마침을 뜻함.
// 한 줄에 문장이 하나이면 생략 가능 vs 한 줄에 두 문장이면 ; 세미콜론 필수.


// 9. 배열 내 자료(요소) 삭제 : 변수명.splice( 삭제할 인덱스 , 개수 ) ,
